# Тема 1 "Строки"
# Важно:
# Срезы (slicing) — получение подстроки
#
# Это один из самых мощных инструментов Python, и его знание отличает уверенного пользователя от новичка. Синтаксис: my_string[начало:конец:шаг].
#
# my_string[2:5] — символы с индекса 2 по 4 включительно (конец не включается, та же логика, что и в range())
# my_string[:5] — если не указать начало, берётся с самого начала строки
# my_string[3:] — если не указать конец, берётся до самого конца строки
# my_string[::2] — шаг 2, то есть каждый второй символ
# my_string[::-1] — классический трюк: шаг -1 разворачивает всю строку задом наперёд. Это не какая-то отдельная функция "reverse", а просто применение среза с отрицательным шагом — очень частый вопрос на собеседованиях "как развернуть строку без использования готовых функций".
#
# Полезные методы строк, которые нужны постоянно:
#
# .lower() / .upper() — привести к нижнему/верхнему регистру. Часто используется перед сравнением строк, чтобы не зависеть от регистра, который ввёл пользователь
# .strip() — убирает пробелы (и переносы строк) по краям строки. Критически важно при работе с input() — пользователь может случайно ввести лишний пробел, и без .strip() сравнение "да " == "да" даст False, хотя визуально это "одно и то же"
# .replace(старое, новое) — заменяет все вхождения одной подстроки на другую
# .split(разделитель) — разбивает строку на список подстрок по разделителю (например, "яблоко,груша,банан".split(",") даст список из трёх слов) — это будет мостиком к следующей теме, спискам
# .join(список) — обратная операция к split, склеивает список строк в одну через заданный разделитель
# len(my_string) — длина строки (это не метод строки, а отдельная встроенная функция, работающая с разными типами)
# .startswith() / .endswith() — проверка, начинается/заканчивается ли строка на определённую подстроку
# in — проверка на вхождение подстроки: "лок" in "яблоко" вернёт True

