# Тема 2 "Списки"
# Методы для изменения списка:

# .append(элемент) — добавляет элемент в конец списка
# .insert(индекс, элемент) — вставляет элемент на конкретную позицию
# .remove(элемент) — удаляет первое найденное совпадение элемента по значению (не по индексу!)
# .pop(индекс) — удаляет элемент по индексу и возвращает его (можно сохранить в переменную); если индекс не указать — удалит и вернёт последний элемент
# del fruits[0] — ещё один способ удаления по индексу (это не метод, а отдельная конструкция языка)
# .sort() — сортирует список на месте (изменяет исходный список, ничего не возвращает)
# sorted(fruits) — а вот это уже отдельная встроенная функция (не метод) — возвращает новый отсортированный список, не трогая исходный. Разница между .sort() и sorted() — частый вопрос на понимание у новичков
# .reverse() — разворачивает список на месте
# len(fruits) — длина списка (та же функция, что и для строк)

